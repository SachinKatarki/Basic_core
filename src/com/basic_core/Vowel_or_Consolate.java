package com.basic_core;

public class Vowel_or_Consolate {
	
	public static void main(String[] args) {
		char alfa = 'a';
		if (alfa == 'a' || alfa == 'e' || alfa == 'o' || alfa == 'i' || alfa == 'u') {
			System.out.println("This is a Vowel");
		} else {
			System.out.println("this is Consonant");
		}
	}
}

	


